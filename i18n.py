class I18n:
    """轻量 i18n 辅助类：支持中英切换与格式化占位符。"""

    def __init__(self, lang='zh'):
        self.lang = lang if lang in LOCALES else 'zh'

    def t(self, key, **kwargs):
        """返回当前语言的翻译；若不存在则返回 key 本身。"""
        text = LOCALES.get(self.lang, LOCALES['zh']).get(key, key)
        if kwargs:
            return text.format(**kwargs)
        return text
